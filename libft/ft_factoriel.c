#include "libft.h"

int		ft_factoriel(int nb)
{
	int		i;
	int		res;

	if (nb == 0)
		return (1);
	i = nb;
	res = nb;
	while (i + 1 > 0)
	{
		res *= i;
		i--;
	}
	return (res);
}
