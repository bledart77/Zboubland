#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t		i;
	char		*s1;

	s1 = (char*)s;
	i = 0;
	while (i < n && s1[i])
	{
		if (s1[i] == c)
			return (&s1[i]);
		i++;
	}
	return (NULL);
}
