int		ft_test8(char *av)
{
	int		i;
	int		n;
	int		j;
	int		b;

	j = 1;
	i = 0;
	b = 0;
	while (av[i])
	{
		n = 0;
		while ((i - b) / 20 != j && av[i])
		{
			if (av[i] == '#')
				n += ft_test41(av, i) + ft_test42(av, i);
			i++;
		}
		b++;
		j++;
		i++;
		if (n < 6)
			return (0);
	}
	return (1);
}
